package com.example.hotrovn.model;

public class ObjectItem {
    String txtCity, txtSocialCare, txtLocation, txtDistraibuted, txtNumberReliefCampaign, txtName, txtPhoneNumber, txtPosition, txtDate, txtTime;

    public ObjectItem(String txtCity, String txtSocialCare, String txtLocation, String txtDistraibuted, String txtNumberReliefCampaign, String txtName, String txtPhoneNumber, String txtPosition, String txtDate, String txtTime) {
        this.txtCity = txtCity;
        this.txtSocialCare = txtSocialCare;
        this.txtLocation = txtLocation;
        this.txtDistraibuted = txtDistraibuted;
        this.txtNumberReliefCampaign = txtNumberReliefCampaign;
        this.txtName = txtName;
        this.txtPhoneNumber = txtPhoneNumber;
        this.txtPosition = txtPosition;
        this.txtDate = txtDate;
        this.txtTime = txtTime;
    }

    public String getTxtCity() {
        return txtCity;
    }

    public void setTxtCity(String txtCity) {
        this.txtCity = txtCity;
    }

    public String getTxtSocialCare() {
        return txtSocialCare;
    }

    public void setTxtSocialCare(String txtSocialCare) {
        this.txtSocialCare = txtSocialCare;
    }

    public String getTxtLocation() {
        return txtLocation;
    }

    public void setTxtLocation(String txtLocation) {
        this.txtLocation = txtLocation;
    }

    public String getTxtDistraibuted() {
        return txtDistraibuted;
    }

    public void setTxtDistraibuted(String txtDistraibuted) {
        this.txtDistraibuted = txtDistraibuted;
    }

    public String getTxtNumberReliefCampaign() {
        return txtNumberReliefCampaign;
    }

    public void setTxtNumberReliefCampaign(String txtNumberReliefCampaign) {
        this.txtNumberReliefCampaign = txtNumberReliefCampaign;
    }

    public String getTxtName() {
        return txtName;
    }

    public void setTxtName(String txtName) {
        this.txtName = txtName;
    }

    public String getTxtPhoneNumber() {
        return txtPhoneNumber;
    }

    public void setTxtPhoneNumber(String txtPhoneNumber) {
        this.txtPhoneNumber = txtPhoneNumber;
    }

    public String getTxtPosition() {
        return txtPosition;
    }

    public void setTxtPosition(String txtPosition) {
        this.txtPosition = txtPosition;
    }

    public String getTxtDate() {
        return txtDate;
    }

    public void setTxtDate(String txtDate) {
        this.txtDate = txtDate;
    }

    public String getTxtTime() {
        return txtTime;
    }

    public void setTxtTime(String txtTime) {
        this.txtTime = txtTime;
    }
}
